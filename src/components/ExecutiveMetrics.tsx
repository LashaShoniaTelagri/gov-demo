const MetricCard: React.FC<MetricCardProps> = ({ title, value, change, changeType, icon, gradient }) => {
  const changeColor = changeType === 'positive' ? 'text-green-600' : changeType === 'negative' ? 'text-red-600' : 'text-gray-600';
  const changeIcon = changeType === 'positive' ? '↑' : changeType === 'negative' ? '↓' : '→';

  return (
    <div className="bg-white rounded-xl shadow-lg p-6 hover:shadow-xl transition-shadow duration-300">
      <div className="flex items-start justify-between mb-4">
        <div className={`w-12 h-12 rounded-lg bg-gradient-to-br ${gradient} flex items-center justify-center text-white shadow-md`}>
          {icon}
        </div>
        <span className={`text-sm font-semibold ${changeColor} flex items-center gap-1`}>
          <span>{changeIcon}</span>
          <span>{change}</span>
        </span>
      </div>
      <h3 className="text-gray-600 text-sm font-medium mb-1">{title}</h3>
      <p className="text-3xl font-bold text-gray-800">{value}</p>
    </div>
  );
};

